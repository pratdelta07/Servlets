package com.infinite.core;

public class ArrayExample1 {
	
	public static void main(String[] args){
		String y[] = {"Monday", "Tuesday", "Wednesday","Thursday"};
		String e = y[2].toUpperCase();
		char t[] = e.toCharArray();
		int p = t.length -1;
		while (p>=0) {
			System.out.print(t[p]);
			p--;
			
		System.out.println();
		}
	}
}

